    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
         if(tblKamar.isRowSelected(tblKamar.getSelectedRow())==true){
            int selectedID = (int)tblKamar.getValueAt(tblKamar.getSelectedRow(), 0);
            
            int result = JOptionPane.showConfirmDialog(
                this,
                "Kamu akan menghapus data Kamar"
                        + tblKamar.getValueAt(tblKamar.getSelectedRow(),1).toString(),
                "Peringatan!",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE
            );
            
            if (result == JOptionPane.YES_OPTION){
                try{
                    Statement stmt = conn.createStatement();
                    stmt.executeUpdate("DELETE FROM kamar WHERE id_kamar='"
                            + selectedID + "'");
                    loadTabelKamar();
                } catch (SQLException e) {
                    System.out.println(e);
                }
            }
         } else {
             JOptionPane.showMessageDialog(this, "Tidak ada yang dipilih");
         }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if(tblKamar.isRowSelected(tblKamar.getSelectedRow())==true){
            int selectedID = (int)tblKamar.getValueAt(tblKamar.getSelectedRow(), 0);
            JTextField txtNo = new JTextField(15);
            JTextField txtHarga = new JTextField(15);
            JTextField txtTttidur = new JTextField(15);
            JTextField txtTkamar = new JTextField(15);
            JTextField txtStatus = new JTextField(15);
            
            
            txtNo.setText(tblKamar.getValueAt(tblKamar.getSelectedRow(), 1).toString());
            txtTttidur.setText(tblKamar.getValueAt(tblKamar.getSelectedRow(), 2).toString());
            txtTkamar.setText(tblKamar.getValueAt(tblKamar.getSelectedRow(), 3).toString());
            txtStatus.setText(tblKamar.getValueAt(tblKamar.getSelectedRow(), 4).toString());
            txtHarga.setText(tblKamar.getValueAt(tblKamar.getSelectedRow(), 5).toString());
            
            JPanel myPanel = new JPanel();
            myPanel.add(new JLabel("Nomor Kamar"));
            myPanel.add(txtNo);
            myPanel.add(new JLabel("Tipe Tempat Tidur"));
            myPanel.add(txtTttidur);
            myPanel.add(new JLabel("Tipe Kamar"));
            myPanel.add(txtTkamar);
            myPanel.add(new JLabel("Harga Kamar"));
            myPanel.add(txtHarga);
            
            int result = JOptionPane.showConfirmDialog(this, myPanel,
                "Edit Data", 
                JOptionPane.OK_CANCEL_OPTION,JOptionPane.PLAIN_MESSAGE);
             
            if (result == JOptionPane.OK_OPTION){
                try{
                    Statement stmt = conn.createStatement();
                    stmt.executeUpdate("UPDATE kamar SET no_kamar='"
                        + txtNo.getText()  + "', tipe_bed='"
                        + txtTttidur.getText() +  "', tipe_kamar='"
                        + txtTkamar.getText() + "', harga='"
                        + txtHarga.getText() + "' WHERE id_kamar='"
                        + selectedID + "'");
                    loadTabelKamar();
                } catch (SQLException e) {
                    System.out.println(e.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Tidak ada yang dipilih");
        }
    }//GEN-LAST:event_btnEditActionPerformed

