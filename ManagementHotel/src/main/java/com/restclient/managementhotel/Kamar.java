    public void loadTabelKamar(){

        String sql = "SELECT * FROM kamar";
        Object[] kolom = {"No", "Nomor Kamar", "Tipe Tempat Tidur",
            "Tipe Kamar", "Status","Harga (Rupiah)"};
        DefaultTableModel dataModel = new DefaultTableModel(null, kolom);
        tblKamar.setModel(dataModel);
        tblKamar.getColumnModel().getColumn(0).setMaxWidth(30);
        
        try{
            Statement stmt = conn.createStatement();
            ResultSet rs   = stmt.executeQuery(sql);
            
            while(rs.next()){    
                int id = rs.getInt("id_kamar");
                String no = rs.getString("no_kamar");
                String ttt = rs.getString("tipe_bed");
                String t_kamar = rs.getString("tipe_kamar");
                String status = rs.getString("occupied");
                int harga = rs.getInt("harga");
                String hrg = "Rp. " + printDigits(harga);
                Object[] data={id, no, ttt, t_kamar, status, hrg};
                dataModel.addRow(data);
            }
        } catch (SQLException e){
            System.out.println(e.getMessage());
        }
    }
    
   public String printDigits(int nilai) {
    return NumberFormat.getNumberInstance(Locale.US).format(nilai);
    }
        public void loadTabelKamar(String teks) throws SQLException{
        Connection conn = Koneksi.connectDB();
        String sql = "SELECT * FROM kamar WHERE no_kamar like '%"+ teks +"%'";
        Object[] kolom = {"No", "Nomor Kamar", "Tipe Tempat Tidur",
            "Tipe Kamar", "Status","Harga"};
        DefaultTableModel dataModel = new DefaultTableModel(null, kolom);
        tblKamar.setModel(dataModel);
        tblKamar.getColumnModel().getColumn(0).setMaxWidth(30);
        
        try{
            Statement stmt = conn.createStatement();
           ResultSet rs   = stmt.executeQuery(sql);
            
            while(rs.next()){
                int id = rs.getInt("id_kamar");
                String no = rs.getString("no_kamar");
                String ttt = rs.getString("tipe_bed");
                String t_kamar = rs.getString("tipe_kamar");
                String status = rs.getString("occupied");
                int harga = rs.getInt("harga");
                
                Object[] data={id, no, ttt, t_kamar, status, harga};
                dataModel.addRow(data);
            }
        } catch (SQLException e){
            System.out.println(e.getMessage());
        }
    }
        
