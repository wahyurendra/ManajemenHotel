    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed

          try {
      //Menyebabkan xml injection
            String no = txtNo.getText();
            String kp = txtKp.getText();
            String np = txtNp.getText();
            String nk = txtNkamar.getText();
            String nr = txtNr.getText();
            int status = cbStatus.getSelectedIndex();
            if (!Pattern.matches("[[0-9]]+",no)){
                JOptionPane.showMessageDialog(null, "Format Nomor Tidak valid");
            } else if 
                  (!Pattern.matches("[[0-9]]+",nk)){
                JOptionPane.showMessageDialog(null, "Format Nomor Kamar Tidak valid");
            } else if
                    (!Pattern.matches("[A-Za-z0-9_-]+",kp)){
                JOptionPane.showMessageDialog(null, "Format Kode Pengunjung Tidak valid");
            
            } else if
                    (!Pattern.matches("[A-Za-z0-9]+",nr)){
                JOptionPane.showMessageDialog(null, "Format Kode Resepsionis Tidak valid");
            }
            
              else {
                    simpanReservasi();
                    Reservasi form = new Reservasi();
                    form.show();
                    this.hide();
                }
            }
            catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed
    private void simpanReservasi() throws Exception {
            String no = txtNo.getText();
            String kp = txtKp.getText();
            String np = txtNp.getText();
            String nk = txtNkamar.getText();
            String nr = txtNr.getText();
            DateFormat dsF = new SimpleDateFormat("yyyy-MM-dd");
            String tgla = dsF.format(tglCin.getDate());
            String tglb = dsF.format(tglCout.getDate());
            int status = cbStatus.getSelectedIndex();
        try {
            Connection conn = Koneksi.connectDB();     
            String sql = "INSERT INTO reservasi (id_reservasi, kode_customer, "
                    + "no_kamar, tgl_checkin, tgl_checkout, kode_recept, status_reservasi) VALUES "
                    + "('" + no + "', '" + kp+ "','" + nk+ "', '"
                    + tgla+ "', '" + tglb +"', '"+status +"');";
            PreparedStatement stmt = conn.prepareStatement("INSERT INTO reservasi (id_reservasi, kode_customer, "
                    + "no_kamar, tgl_checkin, tgl_checkout, kode_recept, status_reservasi) VALUES (?, ?, ?, ?, ?, ?,?)");
            stmt.setString(1, no);
            stmt.setString(2, kp);
            stmt.setString(3, nk);
            stmt.setString(4, tgla);
            stmt.setString(5,tglb);
            stmt.setString(6, nr);
            stmt.setInt(7, status);
            int executeUpdate = stmt.executeUpdate();
            
            String sql1 = "INSERT INTO customer (nama_customer) VALUES "
                    + "('" + no +"');";
            PreparedStatement stmt1 = conn.prepareStatement("INSERT INTO customer (nama_customer) VALUES (?)");
            stmt1.setString(1, np);
            stmt1.executeUpdate();
            
        } catch (java.sql.SQLException ex) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan " + ex);
            return;
        }
        JOptionPane.showMessageDialog(null, "Berhasil Disimpan");
    }
